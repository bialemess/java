    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
     String textoJogosEncontrados = "";
            try {
                ArrayList<Roupa> JogosEncontrados = c1.buscarJogosController(txtBsucarJogo.getText());
                for (Roupa f: JogosEncontrados) {
                    textoJogosEncontrados = textoJogosEncontrados.concat("Nome:" + f.getNome()+ "Código:" + f.getCodigo()+"Categoria:"+f.getCategoria()+ "Preço:" + f.getPreco() + "\n");
                }
                JOptionPane.showMessageDialog(null, textoJogosEncontrados);
            } catch (SQLException ex) {
                Logger.getLogger(frmRoupa.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnBuscarActionPerformed
/*BUSCAR TODOS*/
    private void btnBuscarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarTodoActionPerformed
    ArrayList<Roupa> listaJogo;
        try {
            listaJogo = c1.buscarJogostodosController();
            DefaultTableModel dados = (DefaultTableModel) tblJogo.getModel();
            dados.setNumRows(0);
            for (Roupa f: listaJogo) {
                dados.addRow(new Object[]{f.getNome(), f.getCodigo(), f.getCategoria(), f.getPreco()});
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Não foi possível buscar jogos...");
            System.out.println("Erro ao buscar jogos" + ex.toString());
        }    }//GEN-LAST:event_btnBuscarTodoActionPerformed

