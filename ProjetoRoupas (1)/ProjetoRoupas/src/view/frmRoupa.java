    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        String Nome = this.txtCor.getText();
        int Codigo = Integer.parseInt(this.txtCodigo.getText());
        String Categoria = this.txtCategoria.getText();
        Double preco = Double.parseDouble(this.txtPreco.getText());
        
        try {
            c1.cadastrarJogo(Codigo, Nome, Categoria, preco);
            System.out.println("passou");
        } catch (SQLException ex) {
            Logger.getLogger(frmRoupa.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.txtCodigo.setText("");
        this.txtCor.setText("");
        this.txtCategoria.setText("");
        this.txtPreco.setText("");
        this.txtCor.requestFocus();
        
    }//GEN-LAST:event_btnCadastrarActionPerformed

